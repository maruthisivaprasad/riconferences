                       <h3>
                         About Conference
                       </h3>
                       <p>
                         Welcome to the official website of “World Congress onAdvanced Nursing and Nursing Practice” to be held during August 28-29,2019 at Dubhai, UAE, focusing on the theme “Advanced Concepts and Frameworks for Nursing and Healthcare”. This gathering will offer you special prospects to network with Academic and Clinical Nurse Specialists, Registered Nurses, Clinical Nursing Researchers, Healthcare Doctors and Physicians, Medical Research Departments, Faculty of Health Sciences, C-level executive, Faculty of Healthcare, Deans, Professors and PhD Students from Medical Universities. It is a global platform to discuss and learn about Fostering and Latest Innovations in Nursing and Healthcare. 
                       </p>
                       <p>
                         It is an opportunity to share your best practice initiative, ideas, research project or provide continuing education as it relates current issues and meet the experts in the respective fields. Nursing is a noble profession serving society from decades. One of the most important roles of the nurse is to be a patient advocate  to secure the interests of patients when the patients themselves can't due to ailment or deficient wellbeing information. Nurses are patient educators, responsible for explaining procedures and treatments. Nurses are 'the most trusted healthcare professionals’.
                       </p>
                       <p>
                         The United Arab Emirates (UAE) government aspires to build a world class health system to improve the quality of healthcare and the health outcomes for its population. To achieve this it has implemented extensive health system reforms in the past 10 years. The nature, extent and success of these reforms has not recently been comprehensively reviewed. In this paper we review the progress and outcomes of health systems reform in the UAE.
                       </p>
                       <p>
                        There are around 75 Nursing Works in UAE serving the needs of health care. We can find 29% of General and Surgical Hospitals, 15% of Doctors’ Offices, 13% of Nursing Care Facilities, 10% of Outpatient Facilities and 8% of Home Health Services. In the part of HCP-Gender & Nationality in UAE, we can see that there are around 83% of Female and 16 % of Male Expatriate of Nurses, 62 % of Male and 27% of Female Expatriate of Physicians,7 % of Female and 4 % of Male National of Physicians, 57 % of Male and 34 % of Female Expatriate of Dentists, 7% of Female and 2 % of Male National of Dentists, 52 % of Female and 41% of Male Expatriate of AHPs and 6% Female and 1 % of Male National of AHPs. 
                       </p>
