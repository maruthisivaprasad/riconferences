                       <h3>
                         About Conference
                       </h3>
                       <p>
                         The Aqua fishing and Seafood markets span beyond the concept of quality nutrition, and take an active space in the Ocean Biodiversity and Ecology Managements. 
                       </p>
                       <p>
                         Ri conferences  like to take this opportunity to welcome all of you to The Fisheries and Sea Foods Annual Meeting  which is to be held during August 28-29, 2019 Dubai. Through our theme "Shaping the Future of Food for a Better Tomorrow", the conference will analyse the recent advancements and new modes that can be enforced to the research to take Fisheries and Aquaculture, one step further.
                       </p>
                       <p>
                         Current statistics of active aqua-farming includes around 567 aquatic species, majorly including fish, crustaceans, and aquatic plants and algae.  The farming business is practised almost in every corner of the world; poorest farmers in the developing to biggest fish firms in the developed, fresh water and shallow farming to marine and deep-water fishing, purpose of daily consumption into business exports, farming of fishes and crustaceans to aquatic plants and algae. The fisheries and seafood nutrition has taken a step ahead into the therapeutic market with the associated medicinal values.
                       </p>
                       <p>
                         Due to its active involvement and cross-over with the various verticals, the fisheries and seafood market is to be studies as interrelated market. The major segments would be seafood processing, Sea Food Sustainability, Aquaculture Methods etc...</p>
                         <p>Dubai’s fisheries and seafood market is a high valued fish market and majorly earns from the export unlike the cost-competitive developing market. Dubai’s aqua-fishing is further supported by the government’s active participation and involvement from the aquaculture authorities, ensuring the eco-balanced aqua-fishing and sea food production.

                       </p>
